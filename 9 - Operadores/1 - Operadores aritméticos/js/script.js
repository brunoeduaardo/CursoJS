// Operadores aritméticos

let salario = 100;

console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(2 ** 5);

// Além desses, temos o '++' e o '--', que são operadores de incremento e decremento

let idade = 18;
console.log(++idade);


let idade1 = 18;
console.log(idade1++);
console.log(idade1);


let idade2 = 18;
console.log(--idade2);